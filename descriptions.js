/**
 * Полный статус достоинства планеты в знаке.
 * @returns {'exaltation'|'own'|'friend'|'neutral'|'enemy'|'debilitation'}
 */
function getDignityStatus(planetName, signIndex) {
  if (EXALTATION[planetName] === signIndex) return 'exaltation';
  if (DEBILITATION[planetName] === signIndex) return 'debilitation';
  if (OWN_SIGNS[planetName] && OWN_SIGNS[planetName].includes(signIndex)) return 'own';
  const lord = SIGN_LORDS[signIndex];
  if ((FRIENDS[planetName] || []).includes(lord)) return 'friend';
  if ((ENEMIES[planetName] || []).includes(lord)) return 'enemy';
  return 'neutral';
}

